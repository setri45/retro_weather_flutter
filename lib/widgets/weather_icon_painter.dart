  void drawMatrix(Canvas canvas, Size size, List<List<int>> matrix) {
    final int n = matrix.length;
    final int m = matrix[0].length;
    final double pixelWidth = size.width / m;
    final double pixelHeight = size.height / n;
    for (int y = 0; y < n; y++) {
      for (int x = 0; x < m; x++) {
        final isOn = matrix[y][x] == 1;
        final paint =
            Paint()
              ..color = isOn ? Colors.white : Colors.grey[900]!
              ..style = PaintingStyle.fill;
        canvas.drawCircle(
          Offset(
            x * pixelWidth + pixelWidth / 2,
            y * pixelHeight + pixelHeight / 2,
          ),
          (pixelWidth < pixelHeight ? pixelWidth : pixelHeight) / 3,
          paint,
        );
      }
    }
  }

  @override
  void paint(Canvas canvas, Size size) {
    // Mapping exhaustif de tous les codes météo vers les matrices d'icônes
    List<List<int>> matrix = sunMatrix; // Par défaut

    switch (code) {
      // Soleil/Ciel dégagé
      case 1000:
        matrix = isDay ? sunMatrix : nightMatrix;
        break;

      // Nuages partiels
      case 1003:
        matrix = isDay ? partlyCloudyMatrix : nightPartlyCloudyMatrix;
        break;

      // Nuageux
      case 1006:
        matrix = cloudyMatrix;
        break;

      // Couvert
      case 1009:
        matrix = overcastMatrix;
        break;

      // Brume/Brouillard
      case 1030:
      case 1135:
      case 1147: // Freezing fog
        matrix = mistMatrix;
        break;

      // Pluie légère et variantes
      case 1063: // Patchy rain nearby
      case 1150: // Patchy light drizzle
      case 1153: // Light drizzle
      case 1180: // Patchy light rain
      case 1183: // Light rain
      case 1186: // Moderate rain at times
      case 1189: // Moderate rain
      case 1240: // Light rain shower
        matrix = isDay ? sunRainMatrix : nightRainMatrix;
        break;

      // Pluie forte
      case 1192: // Heavy rain at times
      case 1195: // Heavy rain
      case 1243: // Moderate or heavy rain shower
      case 1246: // Torrential rain shower
        matrix = cloudRainMatrix;
        break;

      // Neige légère et variantes
      case 1066: // Patchy snow nearby
      case 1210: // Patchy light snow
      case 1213: // Light snow
      case 1216: // Patchy moderate snow
      case 1219: // Moderate snow
      case 1255: // Light snow showers
      case 1258: // Moderate or heavy snow showers
        matrix = snowMatrix;
        break;

      // Neige forte
      case 1222: // Patchy heavy snow
      case 1225: // Heavy snow
        matrix = bigSnowMatrix;
        break;

      // Neige soufflée et blizzard
      case 1114: // Blowing snow
      case 1117: // Blizzard
        matrix = smallSnowMatrix;
        break;

      // Bruine glacée et variantes
      case 1069: // Patchy sleet nearby
      case 1072: // Patchy freezing drizzle nearby
      case 1168: // Freezing drizzle
      case 1171: // Heavy freezing drizzle
      case 1198: // Light freezing rain
      case 1201: // Moderate or heavy freezing rain
      case 1204: // Light sleet
      case 1207: // Moderate or heavy sleet
      case 1249: // Light sleet showers
      case 1252: // Moderate or heavy sleet showers
        matrix = rainSnowMatrix;
        break;

      // Grêle
      case 1237: // Ice pellets
      case 1261: // Light showers of ice pellets
      case 1264: // Moderate or heavy showers of ice pellets
        matrix = smallSnowMatrix;
        break;

      // Orage sans précipitations
      case 1087: // Thundery outbreaks nearby
        matrix = thunderMatrix;
        break;

      // Orage avec pluie
      case 1273: // Patchy light rain thunder
      case 1276: // Moderate or heavy rain thunder
        matrix = thunderMatrix;
        break;

      // Orage avec neige
      case 1279: // Patchy light snow thunder
      case 1282: // Moderate or heavy snow thunder
        matrix = thunderSnowMatrix;
        break;

      // Cas par défaut - utiliser la matrice soleil
      default:
        matrix = isDay ? sunMatrix : nightMatrix;
        break;
    }

    drawMatrix(canvas, size, matrix);
  }

  @override
  bool shouldRepaint(covariant WeatherIconPainter oldDelegate) =>
      oldDelegate.code != code || oldDelegate.isDay != isDay;
